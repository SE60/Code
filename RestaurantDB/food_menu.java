package RestaurantDB;

import javafx.scene.image.Image;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;

/**
 *
 * @author l3oat
 */
@Entity
public class food_menu {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    @ManyToOne
    private food_Catalogue cName;
    private String mName;
    private double price;
    private String image;

    public food_menu(food_Catalogue cName,String mName,double price,String image){
        this.cName=cName;
        this.mName=mName;
        this.price=price;
        this.image=image;
    }
    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    public food_Catalogue getcName() {
        return cName;
    }

    public void setcName(food_Catalogue cName) {
        this.cName = cName;
    }
    

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof food_menu)) {
            return false;
        }
        food_menu other = (food_menu) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "RestaurantDB.food_menu[ id=" + id + " ]";
    }
    
}
